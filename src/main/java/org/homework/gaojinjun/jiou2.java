package org.homework.gaojinjun;

public class jiou2 {
    public static void main(String[] args){

        int i = 1;
        while(i<=100){

            if (i%2==0){
                System.out.println(i+"是偶数");
            }
            i++;
        }
        int j =1;
        while(j<=100){
            if (j%2==1){
                System.out.println(j+"是奇数");
            }
            j++;
        }
    }
}
