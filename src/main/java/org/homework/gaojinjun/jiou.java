package org.homework.gaojinjun;

public class jiou {
    public static void main(String[] args){

        for(int i = 1;i<=100;i++) {
            if(i%2==0) {
                System.out.println(i+"是偶数");
            }
        }
        for(int j = 1;j<=100;j++) {
            if(j%2==1) {
                System.out.println(j+"是奇数");
            }
        }

    }
}
